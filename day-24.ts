type Positions = {
  [K in Directions | "pos"]: [number, number];
};

type PadStart<
  T extends number,
  Acc extends number[] = []
> = Acc["length"] extends T ? Acc : PadStart<T, [...Acc, 0]>;

type GetSantaCol<T extends MazeItem[], Acc extends number[] = []> = T extends [
  infer Head extends MazeItem,
  ...infer Tail extends MazeItem[]
]
  ? Head extends Santa
    ? Acc["length"]
    : GetSantaCol<Tail, [...Acc, 0]>
  : never;

type GetSantaPosition<
  T extends MazeMatrix,
  Acc extends number[] = []
> = T extends [infer Head extends MazeItem[], ...infer Tail extends MazeMatrix]
  ? [GetSantaCol<Head>] extends [never]
    ? GetSantaPosition<Tail, [...Acc, 0]>
    : Acc extends [number, ...infer T2 extends number[]]
    ? {
        pos: [Acc["length"], GetSantaCol<Head>];
        up: [T2["length"], GetSantaCol<Head>];
        down: [[...Acc, 0]["length"], GetSantaCol<Head>];
        left: [
          Acc["length"],
          PadStart<GetSantaCol<Head>> extends [
            number,
            ...infer T3 extends number[]
          ]
            ? T3["length"]
            : -1
        ];
        right: [Acc["length"], [...PadStart<GetSantaCol<Head>>, 0]["length"]];
      }
    : {
        pos: [Acc["length"], GetSantaCol<Head>];
        up: [-1, GetSantaCol<Head>];
        down: [[...Acc, 0]["length"], GetSantaCol<Head>];
        left: [
          Acc["length"],
          PadStart<GetSantaCol<Head>> extends [
            number,
            ...infer T3 extends number[]
          ]
            ? T3["length"]
            : -1
        ];
        right: [Acc["length"], [...PadStart<GetSantaCol<Head>>, 0]["length"]];
      }
  : never;

type IsPossible<
  T extends MazeMatrix,
  U extends Directions,
  V extends Positions
> = T[V[U][0]][V[U][1]] extends Tree ? false : true;

type EmptySantaCell<T extends MazeItem[], U> = {
  [K in keyof T]: K extends U ? (T[K] extends Santa ? Alley : T[K]) : T[K];
};

type EmptySantaMatrix<T extends MazeMatrix, U extends Positions> = {
  [K in keyof T]: K extends `${U["pos"][0]}`
    ? EmptySantaCell<T[K], `${U["pos"][1]}`>
    : T[K];
};

type MoveSantaCell<T extends MazeItem[], U> = {
  [K in keyof T]: K extends U ? (T[K] extends Alley ? Santa : T[K]) : T[K];
};

type MoveSantaMatrix<
  T extends MazeMatrix,
  U extends Positions,
  V extends Directions
> = {
  [K in keyof T]: K extends `${U[V][0]}`
    ? MoveSantaCell<T[K], `${U[V][1]}`>
    : T[K];
};

type IsWin<U extends Positions, V extends Directions> = U[V][0] extends -1 | 10
  ? true
  : U[V][1] extends -1 | 10
  ? true
  : false;

type Move<
  T extends MazeMatrix,
  U extends Directions,
  V extends Positions = GetSantaPosition<T>
> = IsWin<V, U> extends true
  ? WinnerMaze
  : IsPossible<T, U, V> extends false
  ? T
  : EmptySantaMatrix<T, V> extends infer Matrix extends MazeMatrix
  ? MoveSantaMatrix<Matrix, V, U>
  : never;

